package exam01;

public class Ex07 {
    public static void main(String[] args) {
        
        // 증감 연산자
        int num = 10;
        int numm = 10;
        int num2 = num++; // 대입 후 연산
        int num3 = ++numm; // 연산 후 대입
        System.out.println("num = " + num + ", num2 = " + num2 + ", num3 = " + num3) ;
    }
}
